package dataStructs.queue.stadium;

class Buyer {
    private String name;
    private int numOfTikets;

    public Buyer(String name, int numOfTikets) {
        this.name = name;
        this.numOfTikets = numOfTikets;
    }

    public String getName() {
        return name;
    }

    public int getNumOfTikets() {
        return numOfTikets;
    }

}
