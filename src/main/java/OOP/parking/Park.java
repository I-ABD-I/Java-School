package OOP.parking;

public class Park {
  private int floorA;
  private int floorB;

  public Park() {
    floorA = 100;
    floorB = 100;
  }

  public int getFloorA() {
    return floorA;
  }

  public void setFloorA(int floorA) {
    this.floorA = floorA;
  }

  public int getFloorB() {
    return floorB;
  }

  public void setFloorB(int floorB) {
    this.floorB = floorB;
  }
}
